package q05;

public class Subtraction {


    public int Subtraction(int a, int b) {
        return a - b;
    }
    public double Subtraction(double a, double b) {
        return a - b;
    }
    public double Subtraction(int a, double b) {
        return a - b;
    }
    public double Subtraction(double a, int b) {
        return a - b;
    }
}
