package q05;

public class Addition {
    private int a;
    private int b;

    public int Addition(int a, int b) {
        return this.a + this.b;
    }
    public double Addition(double a, double b) {
        return this.a + this.b;
    }
    public double Addition(int a, double b) {
        return this.a + this.b;
    }
    public double Addition(double a, int b) {
        return this.a + this.b;
    }
}
